package StringAndStringbuilder;

public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 453.234567f;
        System.out.printf("Formatted number is: %.2f", a);// will print only till 2 decimal
        System.out.println();
        System.out.printf("Hellow my name is %s", "suraj");
        System.out.println();
    }
}
